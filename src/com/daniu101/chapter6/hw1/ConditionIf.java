package com.daniu101.chapter6.hw1;

public class ConditionIf {

    public static void main(String[] args) {

        // 定义周几
        int week = 3;

        if (week == 1) {
            System.out.println("今天公司上班！");

        } else if (week == 2) {
            System.out.println("今天公司上班！");

        } else if (week == 3) {
            System.out.println("今天公司上班！");

        } else if (week == 4) {
            System.out.println("今天公司上班！");

        } else if (week == 5) {
            System.out.println("今天公司上班！");

        } else if (week == 6) {
            System.out.println("今天公司周六放假！");

        } else if (week == 7) {
            System.out.println("今天公司周日放假！");

        } else {
            System.out.println("不是星期，无法判断");
        }

    }

}
